// Helper function to calculate salary increment
export const calculateIncrement = (
  currentSalary: number,
  incrementType: 'amount' | 'percentage',
  incrementValue: number
) => {
  if (incrementType === 'amount') {
    return {
      newSalary: currentSalary + incrementValue,
      incrementAmount: incrementValue,
      incrementPercentage: (incrementValue / currentSalary) * 100,
    };
  } else {
    const incrementAmount = (currentSalary * incrementValue) / 100;
    return {
      newSalary: currentSalary + incrementAmount,
      incrementAmount,
      incrementPercentage: incrementValue,
    };
  }
};

