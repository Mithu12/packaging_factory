export const calculatePayrollTotals = (records) => {
  return records.reduce(
    (totals, record) => ({
      totalEmployees: totals.totalEmployees + 1,
      totalGrossSalary: totals.totalGrossSalary + record.total_earnings,
      totalDeductions: totals.totalDeductions + record.total_deductions,
      totalNetSalary: totals.totalNetSalary + record.net_salary,
    }),
    {
      totalEmployees: 0,
      totalGrossSalary: 0,
      totalDeductions: 0,
      totalNetSalary: 0,
    }
  );
};

