export const calculateAttendanceStats = (records: AttendanceRecord[]) => {
  const stats = {
    total: records.length,
    present: 0,
    absent: 0,
    late: 0,
    half_day: 0,
    early_going: 0,
    work_from_home: 0,
    on_leave: 0,
    holiday: 0,
    week_off: 0,
    compensatory_off: 0,
  };

  records.forEach((record) => {
    switch (record.status) {
      case "present":
        stats.present++;
        break;
      case "absent":
        stats.absent++;
        break;
      case "late":
        stats.late++;
        break;
      case "half_day":
        stats.half_day++;
        break;
      case "early_going":
        stats.early_going++;
        break;
      case "work_from_home":
        stats.work_from_home++;
        break;
      case "on_leave":
        stats.on_leave++;
        break;
      case "holiday":
        stats.holiday++;
        break;
      case "week_off":
        stats.week_off++;
        break;
      case "compensatory_off":
        stats.compensatory_off++;
        break;
    }
  });

  return stats;
};
